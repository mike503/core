function core_validate_password($password = '') {
    if (empty($password) && strlen($password) == 0) {
        return FALSE;
    }
// check length minimum
// check length maximum
    if (preg_replace('/[^A-Za-z0-9-_!@^*()\. ]/', '', $password) == $password) {
        return TRUE;
    }
    return FALSE;
}
